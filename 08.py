'''
calculo de hipotenusa

'''

co = float(input('cateto oposto: '))

ca = float(input('cateto adjacente: '))

hipotenusa = (ca ** 2 + co ** 2) ** (1/2)

print(f'o valor da hipotenusa é:{hipotenusa}')


